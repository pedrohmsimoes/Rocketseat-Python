# Inteiro
numero_Inteiro = 7
print("Inteiro = ", numero_Inteiro)

# Real com ponto flutuante
numero_Real = 3.14
print("Real com ponto flutuante =", numero_Real)

#type() retorna o tipó de variavel
print("tipo de variavel inteiro =", type(numero_Inteiro))
print("tipos de variavel Real com ponto flutuante =", type(numero_Real))

#soma +

soma = 1 + 1
print("1+1 =", soma)

#subtraçao -
subtracao = 1 - 1
print("1-1 = ", subtracao)

#mutiplicacao *
multiplicacao = 4 * 2
print("4 * 2 = ", multiplicacao)

#divisao /
divisao = 5 / 2
print("5 / 2 =", divisao)
print("tipo da variavel", type(divisao))
#int()
print ("valor inteiro aprocimado", int(divisao))

# float()
print("valor em float = ", float(soma))

# modulo %
modulo = 5 % 2
print("valor restante de 5 % 2=", modulo)

#divisao //
divisaoInteira = 5 // 2
print("5 // 2 =", divisaoInteira)
print("tipo da variavel", type(divisaoInteira))